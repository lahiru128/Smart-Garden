package com.example.smartgarden.MODELS;

public class Disease {
    String did;
    String ddesc;
    String deffect;
    String dimage;
    String dname;
    String dplant;
    String dcontrol;

    public Disease() {
    }

    public String getDid() {
        return did;
    }

    public void setDid(String did) {
        this.did = did;
    }

    public String getDdesc() {
        return ddesc;
    }

    public void setDdesc(String ddesc) {
        this.ddesc = ddesc;
    }

    public String getDeffect() {
        return deffect;
    }

    public void setDeffect(String deffect) {
        this.deffect = deffect;
    }

    public String getDimage() {
        return dimage;
    }

    public void setDimage(String dimage) {
        this.dimage = dimage;
    }

    public String getDname() {
        return dname;
    }

    public void setDname(String dname) {
        this.dname = dname;
    }

    public String getDplant() {
        return dplant;
    }

    public void setDplant(String dplant) {
        this.dplant = dplant;
    }

    public String getDcontrol() {
        return dcontrol;
    }

    public void setDcontrol(String dcontrol) {
        this.dcontrol = dcontrol;
    }
}
